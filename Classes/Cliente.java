
public class Cliente {
	
	String nome;
	String endereco;
	int idade;
	
	
	
	public String getNome() {
		return nome;
	}
	 public void setNome(String nome) {
	        this.nome = nome;
	 }
	
	public String getEndereco() {
		return endereco;
	}
	
	public void setEndereço(String endereco) {
        this.endereco = endereco;
    }
	
	public int getIdade() {
		return idade;
	}
	 public void setIdade(int idade) {
	        this.idade = idade;
	    }
}
